RegisterNetEvent('scharman:client:startFindTimer', function(duration)
    LogClient("INFO", "D√©marrage du timer de recherche: " .. duration .. " secondes")
    
    timerActive = true
    
    -- Activer la possibilit√© de sortir du v√©hicule pour le conducteur
    if ClientData.MyRole == "chased" and GetPlayerSeatInVehicle() == -1 then
        canExitVehicle = true
        LogClient("DEBUG", "Le conducteur peut maintenant sortir du v√©hicule")
    end
    
    -- Afficher le timer
    ShowTimer(duration, function(remaining)
        -- Callback pendant le timer
        if remaining % 10 == 0 then
            LogClient("DEBUG", "Temps restant: " .. remaining .. " secondes")
        end
    end, function()
        -- Callback √† la fin du timer
        LogClient("WARN", "Temps √©coul√©!")
        timerActive = false
        canExitVehicle = false
    end)
end)

RegisterNetEvent('scharman:client:stopFindTimer', function()
    LogClient("INFO", "Arr√™t du timer de recherche")
    timerActive = false
    canExitVehicle = false
end)

